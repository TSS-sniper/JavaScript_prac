//WHILE LOOP

let i =0
while (i<=10) {
    //console.log(`Value of i is ${i}`);
    i=i+2
}

let index=0
const myarr = ['a','b','t']

while (index<myarr.length) {
    console.log(`value: ${myarr[index]}`);
    index++
}

//do-while loop

let score = 1

do {
    console.log(`Score is ${score}`); // does work first
    score++
} while (score<=10) //checks condition later